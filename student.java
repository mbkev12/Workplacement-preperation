package inlinefunctions;

class Student
{
    String fullname;
    int Workhours;
 
    Student()
    {
        fullname = null;
        Workhours = 0;
    }
 
    Student (String fullname, int Workhours)
    {
        this.fullname = fullname;
        this.Workhours = Workhours;
    }
}
 
